    def _update(self, key, value):
        self.settings.settings[key] = value
        self.status_bar.set_text(f"Changed: {key}")
        self._update_conditionals()
    
    def on_apply(self, button):
        if self.settings.save_config():
            self.status_bar.set_text("Saved!")
        else:
            self.status_bar.set_text("Error!")
    
    def on_reset(self, button):
        self.settings.reset_to_defaults()
        self._refresh_all()
        self.status_bar.set_text("Reset to defaults")
    
    def _refresh_all(self):
        for key, widget in self.widgets.items():
            val = self.settings.settings.get(key)
            if hasattr(widget, '_scale'):
                widget._scale.get_adjustment().set_value(val)
            elif hasattr(widget, '_switch'):
                widget._switch.set_active(val)
            elif hasattr(widget, '_dropdown'):
                if hasattr(widget, '_options'):
                    for i, (_, v) in enumerate(widget._options):
                        if v == val:
                            widget._dropdown.set_selected(i)
                            break
            elif hasattr(widget, '_entry'):
                widget._entry.set_text(str(val))
        # Update checklist checkbuttons
        for key, checkbuttons in self.checklist_checkbuttons.items():
            val = self.settings.settings.get(key)
            if isinstance(val, list):
                for cb, v in checkbuttons:
                    cb.set_active(v in val)
            else:
                for cb, v in checkbuttons:
                    cb.set_active(v == val)
        self._update_conditionals()
    
    def _update_conditionals(self):
        if 'follow_mouse_threshold' in self.widgets:
            cond_val = self.settings.settings.get('follow_mouse')
            if isinstance(cond_val, bool):
                show = str(cond_val).lower() == '1'
            else:
                show = str(cond_val) == '1'
            self.widgets['follow_mouse_threshold'].set_visible(show)
        if 'mouse_refocus' in self.widgets:
            cond_val = self.settings.settings.get('follow_mouse')
            if isinstance(cond_val, bool):
                show = str(cond_val).lower() == '1'
            else:
                show = str(cond_val) == '1'
            self.widgets['mouse_refocus'].set_visible(show)
        
    def _on_checklist_single_toggle(self, button, key, value):
        if button.get_active():
            if key in self.checklist_checkbuttons:
                for cb, val in self.checklist_checkbuttons[key]:
                    if cb != button:
                        cb.set_active(False)
            self._update(key, value)
    
    def _on_checklist_multi_toggle(self, button, key, value):
        if key not in self.checklist_checkbuttons:
            return
        selected = [val for cb, val in self.checklist_checkbuttons[key] if cb.get_active()]
        self._update(key, selected)



