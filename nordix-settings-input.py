class ButtonRow(Adw.ActionRow):
    """Button that runs a command"""
    def __init__(self, title, subtitle, button_label, command, use_pkexec=False, callback=None):
        super().__init__()
        self.set_title(title)
        if subtitle:
            self.set_subtitle(subtitle)
        self.command = command
        self.use_pkexec = use_pkexec
        self.callback = callback
        
        button = Gtk.Button(label=button_label)
        button.set_valign(Gtk.Align.CENTER)
        button.connect("clicked", self._on_clicked)
        self.add_suffix(button)
    
    def _on_clicked(self, button):
        try:
            if self.use_pkexec:
                cmd = ["pkexec", "bash", "-c", self.command]
            else:
                cmd = ["bash", "-c", self.command]
            
            result = subprocess.run(cmd, capture_output=True, text=True)
            
            if self.callback:
                if result.returncode == 0:
                    self.callback(True, "Command executed successfully")
                else:
                    self.callback(False, result.stderr or "Command failed")
        except Exception as e:
            if self.callback:
                self.callback(False, str(e))


class ButtonBox(Gtk.Box):
    """Row of multiple buttons"""
    def __init__(self, buttons, callback=None):
        super().__init__(orientation=Gtk.Orientation.HORIZONTAL, spacing=8)
        self.set_margin_top(8)
        self.set_margin_bottom(8)
        self.set_margin_start(12)
        self.set_margin_end(12)
        self.set_halign(Gtk.Align.CENTER)
        self.callback = callback
        
        for btn_data in buttons:
            button = Gtk.Button(label=btn_data['label'])
            button.connect("clicked", self._on_clicked, btn_data)
            if btn_data.get('suggested'):
                button.add_css_class("suggested-action")
            if btn_data.get('destructive'):
                button.add_css_class("destructive-action")
            self.append(button)
    
    def _on_clicked(self, button, btn_data):
        command = btn_data.get('command', '')
        use_pkexec = btn_data.get('use_pkexec', False)
        
        try:
            if use_pkexec:
                cmd = ["pkexec", "bash", "-c", command]
            else:
                cmd = ["bash", "-c", command]
            
            result = subprocess.run(cmd, capture_output=True, text=True)
            
            if self.callback:
                if result.returncode == 0:
                    self.callback(True, f"{btn_data['label']}: Success")
                else:
                    self.callback(False, f"{btn_data['label']}: {result.stderr or 'Failed'}")
        except Exception as e:
            if self.callback:
                self.callback(False, str(e))

